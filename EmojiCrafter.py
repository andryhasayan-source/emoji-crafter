def Btn(parent, text, cmd=None, color=None, color_h=None,
        fg='#ffffff', size='m', **kw):
    bg = color   or C['purple']
    bh = color_h or C['purple_h']
    ipy = {'h': 6, 'm': 4, 's': 2}[size]
    b = tk.Button(
        parent, text=text, command=cmd,
        bg=bg, fg=fg,
        activebackground=bh, activeforeground=fg,
        font=F['btn'], relief='flat', bd=0,
        cursor='hand2', padx=12, pady=ipy,
        highlightthickness=0, **kw
    )
    b.bind('<Enter>', lambda e: b.config(bg=bh))
    b.bind('<Leave>', lambda e: b.config(bg=bg))
    return b


