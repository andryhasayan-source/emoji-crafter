class Slider(tk.Canvas):
    """
    Красивый слайдер на Canvas.
    Видимая дорожка, цветное заполнение, круглый ползунок.
    """
    def __init__(self, parent, from_=1, to=10, variable=None,
                 command=None, length=220, **kw):
        bg = kw.pop('bg', C['bg'])
        super().__init__(parent, width=length, height=28,
                         bg=bg, highlightthickness=0, **kw)
        self._min = from_
        self._max = to
        self._var = variable or tk.IntVar(value=from_)
        self._cmd = command
        self._len = length
        self._drag = False

        self._draw()
        self.bind('<ButtonPress-1>',   self._press)
        self.bind('<B1-Motion>',       self._drag_m)
        self.bind('<ButtonRelease-1>', self._release)
        if variable:
            variable.trace_add('write', lambda *a: self._draw())

    def _val_to_x(self, v):
        pad = 10
        return pad + (v - self._min) / (self._max - self._min) * (self._len - 2 * pad)

    def _x_to_val(self, x):
        pad = 10
        ratio = (x - pad) / (self._len - 2 * pad)
        v = round(self._min + ratio * (self._max - self._min))
        return max(self._min, min(self._max, v))

    def _draw(self):
        self.delete('all')
        v   = self._var.get()
        x   = self._val_to_x(v)
        pad = 10
        cy  = 14   # центр по Y
        r   = 9    # радиус ползунка

        # Дорожка — фон (серая)
        self.create_rounded_rect(pad, cy-3, self._len-pad, cy+3,
                                  radius=3, fill=C['sl_track'], outline='')
        # Дорожка — заполнение (фиолетовое)
        self.create_rounded_rect(pad, cy-3, x, cy+3,
                                  radius=3, fill=C['purple'], outline='')
        # Ползунок (белый круг)
        self.create_oval(x-r, cy-r, x+r, cy+r,
                         fill=C['sl_thumb'], outline=C['purple'], width=2)
        # Точка в центре ползунка
        self.create_oval(x-3, cy-3, x+3, cy+3,
                         fill=C['purple'], outline='')

    def create_rounded_rect(self, x1, y1, x2, y2, radius=4, **kw):
        pts = [
            x1+radius, y1,
            x2-radius, y1,
            x2, y1,
            x2, y1+radius,
            x2, y2-radius,
            x2, y2,
            x2-radius, y2,
            x1+radius, y2,
            x1, y2,
            x1, y2-radius,
            x1, y1+radius,
            x1, y1,
        ]
        return self.create_polygon(pts, smooth=True, **kw)

    def _set_val(self, x):
        v = self._x_to_val(x)
        self._var.set(v)
        self._draw()
        if self._cmd:
            self._cmd(str(v))

    def _press(self, e):
        self._drag = True
        self._set_val(e.x)

    def _drag_m(self, e):
        if self._drag:
            self._set_val(e.x)

    def _release(self, e):
        self._drag = False


