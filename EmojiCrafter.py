        if not word or not emoji: messagebox.showwarning("Внимание","Введите слово и эмодзи"); return
        self.dictionary[word] = emoji; self._save_base()
        self._set_status(f"✅  {word} → {emoji}   ({len(self.dictionary)} слов)")
        self._we.delete(0,'end'); self._ee.delete(0,'end'); self._we.focus_set()

