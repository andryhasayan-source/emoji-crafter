    def _decorate(self):
        text = self.inp.get('1.0','end-1c').strip()
        if not text: return
        lang = detect_lang(text)
        dct  = self.dictionary
        if lang == 'en' and self.current_base != ALL_BASES_KEY:
            en_path = os.path.join(self.bases_dir, f"{self.current_base}_en.json")
            if os.path.exists(en_path):
                dct = json.load(open(en_path, encoding='utf-8'))
                self._set_status(f"🌐  Автодетект EN → «{self.current_base}_en»")
        if not dct:
            messagebox.showwarning("Нет базы","Загрузите или создайте базу эмодзи"); return
        result = self.engine.decorate(text, dct,
                                       mode=self.dec_mode.get(),
                                       intensity=self.intensity.get())
        self.out.config(state='normal')
        self.out.delete('1.0','end')
        self.out.insert('1.0', result)
        self.out.config(state='disabled')
        lang_lbl = "EN" if lang == 'en' else "RU"
        self._set_status(
            f"✨  {self.current_base}  ·  {self.dec_mode.get()}  ·  "
            f"интенс. {self.intensity.get()}  ·  {lang_lbl}"
        )

