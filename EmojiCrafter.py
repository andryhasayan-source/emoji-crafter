        self.root.bind('<F1>',             lambda e: self._decorate())
        self.root.bind('<Control-Return>', lambda e: self._decorate())

        def _sa_text(e):
            e.widget.tag_add('sel', '1.0', 'end')
            e.widget.mark_set('insert', 'end')
            return 'break'
        def _sa_entry(e):
            e.widget.select_range(0, 'end')
            e.widget.icursor('end')
            return 'break'
        def _copy(e):
            w = e.widget
            try:
                txt = w.get('sel.first','sel.last') if isinstance(w,tk.Text) else w.selection_get()
                self.root.clipboard_clear(); self.root.clipboard_append(txt)
            except tk.TclError: pass
            return 'break'
        def _cut(e):
            w = e.widget
            if w is self.out: return 'break'
            try:
                if isinstance(w, tk.Text):
                    txt = w.get('sel.first','sel.last'); w.delete('sel.first','sel.last')
                else:
                    txt = w.selection_get()
                    w.delete(w.index(tk.SEL_FIRST), w.index(tk.SEL_LAST))
                self.root.clipboard_clear(); self.root.clipboard_append(txt)
            except tk.TclError: pass
            return 'break'
        def _paste(e):
            w = e.widget
            if w is self.out: return 'break'
            try:
                txt = self.root.clipboard_get()
                if isinstance(w, tk.Text):
                    try: w.delete('sel.first','sel.last')
                    except tk.TclError: pass
                    w.insert('insert', txt)
                else:
                    try: w.delete(w.index(tk.SEL_FIRST), w.index(tk.SEL_LAST))
                    except tk.TclError: pass
                    w.insert('insert', txt)
            except tk.TclError: pass
            return 'break'

        for w in (self.inp, self.out):
            w.bind('<Control-a>', _sa_text); w.bind('<Control-A>', _sa_text)
        for w in (self._we, self._ee):
            w.bind('<Control-a>', _sa_entry); w.bind('<Control-A>', _sa_entry)
        for w in (self.inp, self.out, self._we, self._ee):
            w.bind('<Control-c>', _copy);  w.bind('<Control-C>', _copy)
            w.bind('<Control-x>', _cut);   w.bind('<Control-X>', _cut)
            w.bind('<Control-v>', _paste); w.bind('<Control-V>', _paste)

