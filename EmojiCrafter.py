    def _sentences(self, text):
        parts = re.split(r'([.!?…]+)', text)
        out, i = [], 0
        while i < len(parts):
            s   = parts[i].strip()
            sep = parts[i+1] if i+1 < len(parts) else ''
            if s or sep: out.append((s, sep))
            i += 2
        return out

    def _find_cluster(self, stem):
        """Находит кластер по началу основы слова."""
        for prefix, cluster in self.CLUSTERS.items():
            if stem.startswith(prefix) or prefix.startswith(stem[:4]) if len(stem) >= 4 else False:
                return cluster
        return None

    def _detect_tone(self, words):
        """Определяет тональность предложения."""
        pos_score = neg_score = 0
        for wd in words:
            stem = self._normalize(wd)
            for pw in self.POSITIVE_WORDS:
                if stem.startswith(pw[:5]) if len(pw) >= 5 else stem == pw:
                    pos_score += 1
            for nw in self.NEGATIVE_WORDS:
                if stem.startswith(nw[:5]) if len(nw) >= 5 else stem == nw:
                    neg_score += 1
        if pos_score > neg_score: return 'pos'
        if neg_score > pos_score: return 'neg'
        return 'neutral'

    def _detect_text_theme(self, text, dct):
        """Определяет доминирующую тему всего текста."""
        cluster_counts = {}
        for wd in text.split():
            stem = self._normalize(wd)
            cl = self._find_cluster(stem)
            if cl:
                cluster_counts[cl] = cluster_counts.get(cl, 0) + 1
            # Также проверяем словарь
            if stem in dct or wd.lower() in dct:
                cluster_counts['dict'] = cluster_counts.get('dict', 0) + 1
        if not cluster_counts:
            return None
        return max(cluster_counts, key=cluster_counts.get)

    def _match_word(self, word, dct, used_emojis, used_clusters):
        """
        Многоуровневый подбор эмодзи:
        1. Точное совпадение в словаре
        2. Совпадение основы в словаре
        3. EmojiBrain — эмоциональный анализ слова
        4. Семантический кластер
        """
        stem = self._normalize(word)
        raw  = word.lower()

        # 1. Точное совпадение в пользовательском словаре
        e = dct.get(raw) or dct.get(stem)
        if e and e not in used_emojis:
            used_emojis.add(e)
            return e, 'dict'

        # 2. Частичное совпадение в словаре (основа)
        for key, em in dct.items():
            key_stem = self._normalize(key)
            if (stem.startswith(key_stem[:4]) or key_stem.startswith(stem[:4])) \
               and len(stem) >= 3 and em not in used_emojis:
                used_emojis.add(em)
                return em, 'dict'

        # 3. EmojiBrain — анализ эмоционального веса слова
        brain_emo, brain_intensity = self.brain.get_word_emotion(word)
        if brain_emo and brain_intensity not in ('weak',):
            # Только если эмоция не слабая
            brain_entry = self.brain.EMOJI_TABLE.get(brain_emo, {})
            candidates  = brain_entry.get(brain_intensity, [])
            pool = [x for x in candidates if x not in used_emojis]
            if pool:
                e = pool[hash(raw) % len(pool)]
                used_emojis.add(e)
                return e, 'brain'

        # 4. Семантический кластер
        cl = self._find_cluster(stem)
        if cl and cl not in used_clusters:
            pool = [e for e in self.CLUSTER_EMOJIS.get(cl, []) if e not in used_emojis]
            if pool:
                e = pool[0]
                used_emojis.add(e)
                used_clusters.add(cl)
                return e, cl
        return None, None

    def decorate(self, text, dct, mode='smart', intensity=5):
        if not text.strip(): return text
        # gap — минимальный промежуток между эмодзи (зависит от интенсивности)
        gap = max(1, 10 - intensity)
        return self._process(text, dct, mode=mode, gap=gap)

    def _process(self, text, dct, mode='smart', gap=3):
        """
        Единый умный обработчик для всех режимов.
        Режим влияет только на количество и агрессивность расстановки.
        """
        used_emojis   = set()
        used_clusters = set()
        used_starters = set()
        sentences     = self._sentences(text)
        result        = []

        # Определяем общую тему текста
        text_theme = self._detect_text_theme(text, dct)
        theme_emojis = self.CLUSTER_EMOJIS.get(text_theme, []) if text_theme else []

        for idx, (sent, sep) in enumerate(sentences):
            if not sent.strip():
                if sep: result.append(sep)
                continue

            words    = sent.split()
            tone     = self._detect_tone(words)
            out      = []
            last_pos = -99

            # ── Стартер предложения ──────────────
            starter = None
            if mode in ('smart', 'rich') and idx > 0:
                pool = [s for s in self.STARTERS if s not in used_starters]
                if pool:
                    starter = random.choice(pool)
                    used_starters.add(starter)

            # ── Обработка слов ───────────────────
            for i, wd in enumerate(words):

                # В лёгком режиме — только точные совпадения в словаре
                if mode == 'light':
                    stem = self._normalize(wd)
                    e = dct.get(wd.lower()) or dct.get(stem)
                    if e and e not in used_emojis and i - last_pos >= gap:
                        out.append(wd)
                        out.append(e)
                        used_emojis.add(e)
                        last_pos = i
                        continue
                    out.append(wd)
                    continue

                # Умный и насыщенный режим
                if i - last_pos >= gap:
                    e, source = self._match_word(wd, dct, used_emojis, used_clusters)
                    if e:
                        out.append(wd)
                        out.append(e)
                        last_pos = i
                        continue

                out.append(wd)

            # ── EmojiBrain: умный финальный эмодзи ──
            tail = ''
            # Анализируем предложение через EmojiBrain
            brain_emoji = self.brain.get_sentence_emoji(sent, sep)

            if sep and '!' in sep:
                # Восклицание — EmojiBrain + fallback
                if brain_emoji and brain_emoji not in used_emojis:
                    tail = ' ' + brain_emoji
                    used_emojis.add(brain_emoji)
                else:
                    pool = [x for x in self.EXCLAIM if x not in used_emojis]
                    if pool:
                        e = pool[hash(sent) % len(pool)]
                        used_emojis.add(e)
                        tail = ' ' + e

            elif sep and '?' in sep:
                # Вопрос — EmojiBrain
                if brain_emoji and brain_emoji not in used_emojis:
                    tail = ' ' + brain_emoji
                    used_emojis.add(brain_emoji)
                else:
                    pool = [x for x in self.QUEST if x not in used_emojis]
                    if pool:
                        e = pool[hash(sent) % len(pool)]
                        used_emojis.add(e)
                        tail = ' ' + e

            elif sep and '…' in sep or (sep and '.' in sep and mode == 'rich'):
                # Многоточие или конец предложения в насыщенном режиме
                if brain_emoji and brain_emoji not in used_emojis:
                    tail = ' ' + brain_emoji
                    used_emojis.add(brain_emoji)

            elif mode == 'rich' and len(words) > 3:
                # Насыщенный режим — финал каждого предложения
                if brain_emoji and brain_emoji not in used_emojis:
                    tail = ' ' + brain_emoji
                    used_emojis.add(brain_emoji)
                elif theme_emojis:
                    pool = [x for x in theme_emojis if x not in used_emojis]
                    if pool:
                        e = pool[0]
                        used_emojis.add(e)
                        tail = ' ' + e

            elif mode == 'smart' and brain_emoji and brain_emoji not in used_emojis:
                # Умный режим — добавляем только если эмоция достаточно сильная
                emotions = self.brain.analyze_text(sent)
                dom_emo, dom_val = self.brain._dominant(emotions)
                if dom_val >= 0.4:  # порог для умного режима
                    tail = ' ' + brain_emoji
                    used_emojis.add(brain_emoji)

            built = ' '.join(out)
            if starter: built = starter + ' ' + built
            result.append(built + tail + sep)

        return ' '.join(result).strip()


