    def _import_txt(self):
        path = filedialog.askopenfilename(title="Выберите .txt базу",
            filetypes=[("Текстовые файлы","*.txt"),("Все файлы","*.*")])
        if not path: return
        name = os.path.splitext(os.path.basename(path))[0]
        dest = os.path.join(self.bases_dir,f"{name}.json")
        words = {}; errors = 0
        try:
            with open(path,'r',encoding='utf-8') as f:
                for raw in f:
                    line = raw.strip()
                    if not line or line.startswith('#'): continue
                    parsed = False
                    for sep in ('→','->','\t',' : '):
                        if sep in line:
                            pts = line.split(sep,1)
                            if len(pts)==2:
                                wd=pts[0].strip().lower(); em=pts[1].strip()
                                if wd and em: words[wd]=em; parsed=True
                            break
                    if not parsed:
                        pts=line.rsplit(None,1)
                        if len(pts)==2: words[pts[0].strip().lower()]=pts[1].strip()
                        else: errors+=1
        except Exception as ex: messagebox.showerror("Ошибка",str(ex)); return
        if not words: messagebox.showwarning("Пусто","Не найдено пар «слово→эмодзи»"); return
        if os.path.exists(dest):
            if not messagebox.askyesno("Перезаписать?",f"«{name}» уже есть.\nПерезаписать?"): return
        with open(dest,'w',encoding='utf-8') as f: json.dump(words,f,ensure_ascii=False,indent=2)
        self._load_base(name); self._refresh_list()
        msg = f"Импортировано {len(words)} слов в «{name}»."
        if errors: msg += f"\nПропущено: {errors}"
        messagebox.showinfo("Готово", msg)

    def _add_word(self):
        if self.current_base == ALL_BASES_KEY:
            messagebox.showwarning("Внимание","В режиме «Все базы» выберите конкретную базу."); return
